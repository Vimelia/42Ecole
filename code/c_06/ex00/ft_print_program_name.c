#include <unistd.h>

int	main(int ac, char **ar)
{
	int	i;

	i = 0;
	if (ac == 1)
	{
		while (ar[0][i] != '\0')
		{
			write(1, &ar[0][i], 1);
			i++;
		}
	}
}
